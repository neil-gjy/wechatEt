package com.tjport.common.wechat.menu;

/**
 * 个性化规则
 * @author Neil
 * Date: 2016-05-25
 */

public class Matchrule {
	
	private int tag_id;
	private int sex;
	private String contoury;
	private String city;
	private String client_platform_type;
	private String language;
	
	public int getTag_id() {
		return tag_id;
	}
	public void setTag_id(int tag_id) {
		this.tag_id = tag_id;
	}
	
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public String getContoury() {
		return contoury;
	}
	public void setContoury(String contoury) {
		this.contoury = contoury;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getClient_platform_type() {
		return client_platform_type;
	}
	public void setClient_platform_type(String client_platform_type) {
		this.client_platform_type = client_platform_type;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	
	
	
}
